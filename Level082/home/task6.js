const arr = ["JS", "React", "Node"];

console.log("Using for...in:");
for (let key in arr) {
  console.log(key);
}

console.log("Using for...of:");
for (let value of arr) {
  console.log(value);
}



   //
  // for...in loops over the keys (indexes) of the array
 //
   //
  // for...of loops over the values of the array
 //  
  